// Using var for declaration
var a = 10;
var b = 5;

var addition = a + b;
var subtraction = a - b;
var multiplication = a * b;
var division = a / b;

console.log("Using var:");
console.log("Addition: " + addition);
console.log("Subtraction: " + subtraction);
console.log("Multiplication: " + multiplication);
console.log("Division: " + division);

// Using let for declaration
let x = 20;
let y = 4;

let add = x + y;
let subtract = x - y;
let multiply = x * y;
let divide = x / y;

console.log("Using let:");
console.log("Addition: " + add);
console.log("Subtraction: " + subtract);
console.log("Multiplication: " + multiply);
console.log("Division: " + divide);

// Using const for declaration
const m = 15;
const n = 3;

const addConst = m + n;
const subtractConst = m - n;
const multiplyConst = m * n;
const divideConst = m / n;

console.log("Using const:");
console.log("Addition: " + addConst);
console.log("Subtraction: " + subtractConst);
console.log("Multiplication: " + multiplyConst);
console.log("Division: " + divideConst);
